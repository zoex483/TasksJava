package seminar03.task02;

public class Product {
    public String name;
    public String country;
    public double weight;
    public double price;
    public int grade;

    public Product(String name, String country, double weight, double price, int grade) {
        this.name = name;
        this.country = country;
        this.weight = weight;
        this.price = price;
        this.grade = grade;
    }
}
